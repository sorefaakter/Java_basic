package com.bd.batch13;

public class Variables {

	public static void main(String[] args) {
		//Data-type variableName = value; 
		String name = "Batch-11";
		int numberInt = 101;
		float numberFloat = 10.25f;
		boolean result = true;
		System.out.println("String Value: "+name);
		System.out.println("Integer Number: "+numberInt);
		System.out.println("Float Number: "+numberFloat);
		System.out.println("Boolean: "+result);
		System.out.println("Integer Number: "+numberInt+" Float Number: "+numberFloat);
	}

}
